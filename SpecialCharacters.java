public class SpecialCharacters {
    public static void main(String[] args) {
        System.out.println("Line1\nLine2");       // سطر جديد
        System.out.println("Column1\tColumn2");   // مسافة جدولة
        System.out.println("He said \"Hello\"");  // علامة اقتباس
        System.out.println("Path: C:\\Users\\Mohammed"); // Backslash
    }
}