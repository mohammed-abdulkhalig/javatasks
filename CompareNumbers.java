public class CompareNumbers {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 20;

        if (num1 > num2) {
            System.out.println("num1 bigger");
        } else if (num1 < num2) {
            System.out.println("num2 bigger");
        } else {
            System.out.println("both numbers are equal");
        }
    }
}